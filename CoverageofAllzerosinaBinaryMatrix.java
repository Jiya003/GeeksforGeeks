class Solution {
    public int findCoverage(int[][] matrix) {
        // code here
        int c=0;
        for(int i=0;i<matrix.length;i++){
            for(int j=0;j<matrix[i].length;j++){
                if(matrix[i][j]==0){
                    if(i>0 && matrix[i-1][j]==1) c++;
                    if(j>0 && matrix[i][j-1]==1) c++;
                    if(i<matrix.length-1 && matrix[i+1][j]==1) c++;
                    if(j<matrix[i].length-1 && matrix[i][j+1]==1) c++;
                }
            }
        }
        
        return c;
    }
}
